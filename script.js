//  Conditional Statements

let x = 40;
let y = 2;
let z = 50;

// if Condition
if (x > y) {
  console.log(x + ' Greater than ' + y);
}

// if ...else Condition
if (x > z) {
  console.log(x + ' Greater than ' + z);
} else {
  console.log(z + ' Greater than ' + x);
}

// else if Condition
if (243 > 40) {
  console.log('First Condition Met');
} else if (243 < 40) {
  console.log('Second Condition Met');
} else {
  console.log('None of above condtion met');
}
